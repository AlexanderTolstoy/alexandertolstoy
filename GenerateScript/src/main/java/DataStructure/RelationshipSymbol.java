public class RelationshipSymbol {
    String Rect;
    String ListOfPoints;
    String CornerStyle;
    String ArrowStyle;
    String LineColor;
    String DashStyle;
    String ShadowColor;

    String SourceSymbol;
    String DestinationSymbol;
    String Relationship;

    public String getRect() {
        return Rect;
    }

    public void setRect(String rect) {
        Rect = rect;
    }

    public String getListOfPoints() {
        return ListOfPoints;
    }

    public void setListOfPoints(String listOfPoints) {
        ListOfPoints = listOfPoints;
    }

    public String getCornerStyle() {
        return CornerStyle;
    }

    public void setCornerStyle(String cornerStyle) {
        CornerStyle = cornerStyle;
    }

    public String getArrowStyle() {
        return ArrowStyle;
    }

    public void setArrowStyle(String arrowStyle) {
        ArrowStyle = arrowStyle;
    }

    public String getLineColor() {
        return LineColor;
    }

    public void setLineColor(String lineColor) {
        LineColor = lineColor;
    }

    public String getDashStyle() {
        return DashStyle;
    }

    public void setDashStyle(String dashStyle) {
        DashStyle = dashStyle;
    }

    public String getShadowColor() {
        return ShadowColor;
    }

    public void setShadowColor(String shadowColor) {
        ShadowColor = shadowColor;
    }

    public String getSourceSymbol() {
        return SourceSymbol;
    }

    public void setSourceSymbol(String sourceSymbol) {
        SourceSymbol = sourceSymbol;
    }

    public String getDestinationSymbol() {
        return DestinationSymbol;
    }

    public void setDestinationSymbol(String destinationSymbol) {
        DestinationSymbol = destinationSymbol;
    }

    public String getRelationship() {
        return Relationship;
    }

    public void setRelationship(String relationship) {
        Relationship = relationship;
    }
}
